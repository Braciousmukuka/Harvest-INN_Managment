<script>
document.addEventListener('DOMContentLoaded', function() {
    // Auto-dismiss alerts after 5 seconds
    const autoDissmissAlerts = document.querySelectorAll('.auto-dismiss-alert');
    autoDissmissAlerts.forEach(function(alert) {
        // Set timeout to remove the alert after 5 seconds
        setTimeout(function() {
            alert.style.transition = 'opacity 0.5s ease-out';
            alert.style.opacity = '0';
            setTimeout(function() {
                alert.remove();
            }, 500); // Wait for fade out animation to complete
        }, 5000); // 5 seconds
    });
    
    // Manual dismiss functionality
    const dismissButtons = document.querySelectorAll('.dismiss-btn');
    dismissButtons.forEach(function(button) {
        button.addEventListener('click', function() {
            const alert = this.closest('.auto-dismiss-alert');
            alert.style.transition = 'opacity 0.5s ease-out';
            alert.style.opacity = '0';
            setTimeout(function() {
                alert.remove();
            }, 500);
        });
    });
});
</script>
